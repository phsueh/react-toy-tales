import React from 'react';
import './App.css';

import Header from './components/Header'
import ToyForm from './components/ToyForm'
import ToyContainer from './components/ToyContainer'

//Draw out app hierarchy
//Hook up data from db.json file to React components 
  //Render ToyCard component on page
//Extra - build functionaltiiy to add new ToyCard
//Extra - build delete functionality 
//Extra - build like functionality

class App extends React.Component{

  state = {
    display: false
  }

  handleClick = () => {
    let newBoolean = !this.state.display
    this.setState({
      display: newBoolean
    })
  }

  render(){
    return (
      <>
        <Header/>
        { this.state.display
            ?
          <ToyForm/>
            :
          null
        }
        <div className="buttonContainer">
          <button onClick={this.handleClick}> Add a Toy </button>
        </div>
        <ToyContainer/>
      </>
    );
  }

}

export default App;
